class QuesoChihuahua:

    def __init__():
        self.melting = 20
        self.melted = false
        self.temperature = 0

    def isMelted():
        return self.melted

    def getCurrentTemperature():
        return self.temperature

    def getMeltingTemperature():
        return self.melting

    def setCurrentTemperature(temp):
        self.temperature = temp

    def melt(melted):
        self.melted = melted
